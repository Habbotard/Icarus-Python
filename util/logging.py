

def info(output_line):
    """
    Prints output line with [INFO] tag
    :param output_line: the line to print
    :return:
    """
    print ("[INFO]: " + output_line)
    return


def error(output_line):
    """
    Prints output line with [ERROR] tag
    :param output_line: the line to print
    :return:
    """
    print ("[ERROR]: " + output_line)
    return


